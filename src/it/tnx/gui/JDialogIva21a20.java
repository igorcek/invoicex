    private void procediActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procediActionPerformed
        try {
            if (!(DbUtils.containRows(Db.getConn(), "select * from codici_iva where codice = '20'"))) {
                DbUtils.tryExecQuery(Db.getConn(), "insert into codici_iva set codice = '20', percentuale = 20, descrizione = 'Iva 20%', descrizione_breve = 'Iva 20%'");
            }
        } catch (Exception e) {
            e.printStackTrace();
            SwingUtils.showExceptionMessage(this, e);
        }
        try {
            if (DbUtils.containRows(Db.getConn(), "select * from codici_iva where codice = '20'")) {
                //articoli
                try {
                    DbUtils.tryExecQuery(Db.getConn(), "update articoli set iva = 20 where iva = 21");
                } catch (Exception e) {
                    e.printStackTrace();
                    SwingUtils.showExceptionMessage(this, e);
                }
                //clienti e fornitori
                try {
                    DbUtils.tryExecQuery(Db.getConn(), "update clie_forn set iva_standard = 20 where iva_standard = 21");
                } catch (Exception e) {
                    e.printStackTrace();
                    SwingUtils.showExceptionMessage(this, e);
                }
                //impostazioni
                try {
                    if (CastUtils.toString(InvoicexUtil.getIvaDefault()).equals("21")) {
                        DbUtils.tryExecQuery(Db.getConn(), "update dati_azienda set codiceIvaDefault = '20'");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                    SwingUtils.showExceptionMessage(this, e);
                }
                try {
                    if (CastUtils.toString(InvoicexUtil.getIvaSpese()).equals("21")) {
                        DbUtils.tryExecQuery(Db.getConn(), "update dati_azienda set codiceIvaSpese = '20'");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                    SwingUtils.showExceptionMessage(this, e);
                }
            } else {
                SwingUtils.showErrorMessage(this, "Impossibile trovare il codice iva 20");
                stato.setForeground(Color.RED);
                stato.setText("Errore nel passaggio, non Ã¨ stato creato il codice iva 20");
                return;
            }
        } catch (Exception e) {
            SwingUtils.showExceptionMessage(this, e);
        }
        procedi.setEnabled(false);
        stato.setForeground(Color.GREEN);
        stato.setText("Passaggio completato");
        main.fileIni.setValue("iva21a20", "eseguito", true);
        try {
            DbUtils.tryExecQuery(Db.getConn(), "update dati_azienda set iva21a20eseguito = 'S'");
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_procediActionPerformed

