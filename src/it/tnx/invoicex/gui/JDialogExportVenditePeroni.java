        SwingUtils.mouse_wait(this);
        try {
            bp = new BirraPeroni(this);
            bp.start();
            System.out.println("file: " + bp.nomefile + " in: " + bp.cartella);
            Util.start2(bp.cartella);
            SwingUtils.mouse_def(this);
            SwingUtils.showInfoMessage(main.getPadreFrame(), "Generato il file '" + bp.nomefile + "'");
        } catch (Exception ex) {
            ex.printStackTrace();
            SwingUtils.mouse_def(this);
            SwingUtils.showErrorMessage(main.getPadreFrame(), ex.toString());
        }        
