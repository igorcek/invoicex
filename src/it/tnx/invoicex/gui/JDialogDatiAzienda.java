    private void leggiDatiDaDb() {
        ResultSet r = Db.openResultSet("select " + main.campiDatiAzienda + " from dati_azienda");
        try {
            r.next();
            texRagioneSociale.setText(r.getString("ragione_sociale"));
            texPartitaIva.setText(r.getString("piva"));
            texCodiceFiscale.setText(r.getString("cfiscale"));
            texIndirizzo.setText(r.getString("indirizzo"));
            texCap.setText(r.getString("cap"));
            texLocalita.setText(r.getString("localita"));
            texProv.setText(r.getString("provincia"));
            texTelefono.setText(r.getString("telefono"));
            texFax.setText(r.getString("fax"));
            texWeb.setText(r.getString("sito_web"));
            texEmail.setText(r.getString("email"));
        } catch (Exception err) {
            err.printStackTrace();
        }
    }
    
    private void scriviDatiSuDb() {
        try {
            Map m = new HashMap();
            m.put("ragione_sociale", texRagioneSociale.getText());
            m.put("piva", texPartitaIva.getText());
            m.put("cfiscale", texCodiceFiscale.getText());
            m.put("indirizzo", texIndirizzo.getText());
            m.put("cap", texCap.getText());
            m.put("localita", texLocalita.getText());
            m.put("provincia", texProv.getText());
            m.put("telefono", texTelefono.getText());
            m.put("fax", texFax.getText());
            m.put("sito_web", texWeb.getText());
            m.put("email", texEmail.getText());
            m.put("flag_dati_inseriti", "S");
            String sql = "update dati_azienda set " + DbUtils.prepareSqlFromMap(m);
            DbUtils.tryExecQuery(Db.getConn(), sql);
        } catch (Exception err) {
            err.printStackTrace();
            SwingUtils.showExceptionMessage(main.getPadreFrame(), err);
        }        
    }
    
    boolean controllaDati() {
        if (texRagioneSociale.getText().length() == 0) {
            texRagioneSociale.requestFocus();
            JOptionPane.showMessageDialog(this, "La ragione sociale non Ã¨ inserita", "Attenzione", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (texRagioneSociale.getText().length() <= 3) {
            texRagioneSociale.requestFocus();
            JOptionPane.showMessageDialog(this, "La ragione sociale Ã¨ troppo breve", "Attenzione", JOptionPane.INFORMATION_MESSAGE);
            return false;            
        }
        if (texPartitaIva.getText().length() == 0) {
            texPartitaIva.requestFocus();
            JOptionPane.showMessageDialog(this, "La partita iva non Ã¨ inserita", "Attenzione", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }        
        if (texPartitaIva.getText().length() < 11) {
            texPartitaIva.requestFocus();
            JOptionPane.showMessageDialog(this, "La partita iva Ã¨ troppo breve", "Attenzione", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        return true;
    }
    
