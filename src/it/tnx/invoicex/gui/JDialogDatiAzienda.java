    private void butConfermaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butConfermaActionPerformed
        if (controllaDati()) {
            scriviDatiSuDb();
            gestioneFatture.main.attivazione.setDatiAziendaInseriti(true);
            
            //memorizzo i dati sull'intestazione dei documenti
            if (JOptionPane.showConfirmDialog(this, "Vuoi aggiungere questi dati sulle intestazioni dei documenti in stampa ?\nSe l'intestazione delle stampe era giÃ  corretta clicca su No", "Attenzione", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    String sql = "update dati_azienda set ";
                    sql += " intestazione_riga1 = " + Db.pc(texRagioneSociale.getText(), "VARCHAR");
                    sql += " ,intestazione_riga2 = " + Db.pc(texIndirizzo.getText(), "VARCHAR");
                    sql += " ,intestazione_riga3 = " + Db.pc(texCap.getText() + " " + texLocalita.getText() + " (" + texProv.getText() + ")", "VARCHAR");
                    sql += " ,intestazione_riga4 = " + Db.pc("Tel. " + texTelefono.getText() + " / Fax " + texFax.getText(), "VARCHAR");
                    if (texWeb.getText().length() > 0) {
                        if (texEmail.getText().length() > 0) {
                            sql += " ,intestazione_riga5 = " + Db.pc(texWeb.getText() + " / " + texEmail.getText(), "VARCHAR");
                        } else {
                            sql += " ,intestazione_riga5 = " + Db.pc(texWeb.getText(), "VARCHAR");
                        }
                    }
                    if (texPartitaIva.getText().length() > 0) {
                        if (texCodiceFiscale.getText().length() > 0) {
                            sql += " ,intestazione_riga6 = " + Db.pc("P.IVA " + texPartitaIva.getText() + " - Cod. Fiscale " + texCodiceFiscale.getText(), "VARCHAR");
                        } else {
                            sql += " ,intestazione_riga6 = " + Db.pc("P.IVA " + texPartitaIva.getText(), "VARCHAR");
                        }
                    }
                    Db.executeSql(sql);
                } catch (Exception err) {
                    err.printStackTrace();
                }            
            }
            
            dispose();
        }
    }//GEN-LAST:event_butConfermaActionPerformed

