    private void butCreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butCreaActionPerformed
        this.coord = new CoordinateBancarie();

        String temp;
        temp = String.valueOf(griBanc.getValueAt(griBanc.getSelectedRow(), 0));
        this.coord.setAbi(temp);
        temp = String.valueOf(griBanc.getValueAt(griBanc.getSelectedRow(), 2));
        this.coord.setCab(temp);
        temp = String.valueOf(griBanc.getValueAt(griBanc.getSelectedRow(), 5));
        this.coord.setCc(temp);

        codSia = texSia.getText();
        if(codSia.length() != 5){
            javax.swing.JOptionPane.showMessageDialog(this, "Codice Sia non corretto, deve essere di 5 caratteri");
            return;
        }

        codSiaCred = texSiaCred4.getText();
        if(codSiaCred.length() != 5){
            javax.swing.JOptionPane.showMessageDialog(this, "Codice Sia non corretto, deve essere di 5 caratteri");
            return;
        }

        supporto = texSupporto4.getText();
        if(supporto.length() > 20){
            javax.swing.JOptionPane.showMessageDialog(this, "Supporto non corretto, deve essere di massimo 20 caratteri");
            return;
        } else {
            for(int i = 0; i < 20-supporto.length(); i++){
                supporto += " ";
            }
        }

        freeText = texFreeText4.getText();
        if(freeText.length() > 5){
            javax.swing.JOptionPane.showMessageDialog(this, "Supporto non corretto, deve essere di massimo 20 caratteri");
            return;
        } else {
            for(int i = 0; i < 5-freeText.length(); i++){
                freeText += " ";
            }
        }
        
        this.dispose();
    }//GEN-LAST:event_butCreaActionPerformed

