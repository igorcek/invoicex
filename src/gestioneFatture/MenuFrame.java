    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String lef = UIManager.getLookAndFeel().getName();
        Color backColor = getMenuPanel().desktop.getBackground();
        if (lef.startsWith("Substance")) {
            backColor = UIManager.getColor("Label.background");
        }
        int somma = (backColor.getBlue() + backColor.getGreen() + backColor.getRed()) / 3;
        if (somma > 130) {
            lblInfoLoading2.setForeground(Color.black);
            labmess2.setForeground(Color.black);
        } else {
            lblInfoLoading2.setForeground(Color.white);
            labmess2.setForeground(Color.white);
        }
    }//GEN-LAST:event_formWindowOpened

