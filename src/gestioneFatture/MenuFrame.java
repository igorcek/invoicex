    public void aggiornaTitle() {
        setTitle("Invoicex " + (main.versione == null ? "" : main.versione) + " - ver. " + main.version.toString() + " (" + MenuPanel.getBuildH(main.build) + ")" + " - Database: " + Db.dbNameDB);
        if (main.attivazione.getIdRegistrazione() != null && main.attivazione.getIdRegistrazione() > 0) {
            setTitle(getTitle() + " - id " + main.attivazione.getIdRegistrazione());
        }
        if (main.utente != null) {
            setTitle(getTitle() + " - utente: " + main.utente.getNomeUtente());
        }
        try {
            DatiAzienda dati = main.attivazione.getDatiAzienda();
            if (dati != null) {
                if (dati.getRagione_sociale() != null) {
                    setTitle(getTitle() + " - " + StringUtils.abbreviate(dati.getRagione_sociale(), 50));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

