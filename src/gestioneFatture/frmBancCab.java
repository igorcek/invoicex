    public frmBancCab(String codiceAbi, String nomeBanca) {
        this.codiceAbi = codiceAbi;
        this.nomeBanca = nomeBanca;
        initComponents();

        //associo il panel ai dati
        this.dati.dbNomeTabella = "banche_cab";

        Vector chiave = new Vector();
        chiave.add("abi");
        chiave.add("cab");
        this.dati.dbChiave = chiave;
        this.dati.butSave = this.butSave;
        this.dati.butUndo = this.butUndo;
        this.dati.butFind = this.butFind;
        this.dati.dbOpen(Db.getConn(), "select * from banche_cab where abi = '" + codiceAbi + "'");
        this.dati.dbRefresh();

        //apro la griglia
        //this.griglia.dbEditabile = true;
        this.griglia.dbChiave = chiave;
        this.griglia.flagUsaThread = false;

        java.util.Hashtable colsWidthPerc = new java.util.Hashtable();
        colsWidthPerc.put("abi", new Double(10));
        colsWidthPerc.put("cab", new Double(10));
        colsWidthPerc.put("cap", new Double(10));
        colsWidthPerc.put("comune", new Double(30));
        colsWidthPerc.put("indirizzo", new Double(30));
        this.griglia.columnsSizePerc = colsWidthPerc;
        String sql = "select abi, banche_cab.cab, cap, comuni.comune, indirizzo from banche_cab left join comuni on codice_comune = comuni.codice where abi = '" + codiceAbi + "'";
        System.out.println("sql:" + sql);
        this.griglia.dbOpen(Db.getConn(), sql);
        this.griglia.dbPanel = this.dati;

        //sistemo alcune cose
        //this.comTipo.addItem("CL");
        //this.comTipo.addItem("FO");
        this.comComu.dbOpenList(Db.getConn(), "Select comune, codice from comuni order by comune");
        this.dati.dbRefresh();

        if (dati.isOnSomeRecord == false) {
            javax.swing.JOptionPane.showMessageDialog(this, "Non ci sono agenzie caricate per questa banca, prosegui inserendone una", "Attenzione", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            butNewActionPerformed(null);
        }

        this.labClie.setText("Agenzie della banca: " + codiceAbi + " - " + nomeBanca);
    }

